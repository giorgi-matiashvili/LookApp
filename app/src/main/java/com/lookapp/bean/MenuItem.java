package com.lookapp.bean;

public class MenuItem {
	private long id;
	private String name;
	private String description;
	private String nameKa;
	private String descriptionKa;
	private double price;
	private String nameToShow;
	private String descriptionToShow;
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getNameKa() {
		return nameKa;
	}
	public void setNameKa(String nameKa) {
		this.nameKa = nameKa;
	}
	public String getDescriptionKa() {
		return descriptionKa;
	}
	public void setDescriptionKa(String descriptionKa) {
		this.descriptionKa = descriptionKa;
	}
	public String getNameToShow() {
		return nameToShow;
	}
	public void setNameToShow(String nameToShow) {
		this.nameToShow = nameToShow;
	}
	public String getDescriptionToShow() {
		return descriptionToShow;
	}
	public void setDescriptionToShow(String descriptionToShow) {
		this.descriptionToShow = descriptionToShow;
	}
	
	
	
	
	
	

}
